# To print the abbreviation of user input.

s = input("Enter the abbreviation: ")

if s == "lol":
    print("Laughing Out Loud")
elif s == "rofl":
    print("Rolling On the Floor Laughing")
elif s == "lmk":
    print("Let Me Know")
elif s == "smh":
    print("Shaking my head")
else:
    print("Please enter a valid abbreviation...")


